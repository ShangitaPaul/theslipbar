// Toggle mobile navigation links
function toggleMobileNav() {
    const mobileNavLinks = document.querySelector('.mobile-nav-links');
    mobileNavLinks.style.display = (mobileNavLinks.style.display === 'flex') ? 'none' : 'flex';
}
  
